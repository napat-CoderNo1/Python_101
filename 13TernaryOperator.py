# Ternary Operator ย่อให้ if ด้านบน สั้นลง โดยให้ผลเหมือนเดิม

'''
"True Condition" if expression else "Other Condition"

'''

age2 = int(input("Enter your age2 : "))
print("Old") if age2 >= 60 else print("Young") 