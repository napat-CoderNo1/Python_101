# Modify Data การเเก้ไขข้อมูล
# เปลื่ยนเเปลงข้อมูลได้ mutable
# เปลื่ยนเเปลงไม่ได้ imutable

groupOfNumber = [1,2,3,4,5,6]
# การเเก้ไข
# ชื่อตัวเเปร [index] = "ข้อมูลที่เเก้ไข"
print("Before Modify List => ",groupOfNumber)

groupOfNumber[2] = 9
groupOfNumber[5] = 10

print("After Modify List  => ",groupOfNumber)